function helloWorld(userName) {
    return "Hello " + userName;
}

console.log(helloWorld("Phúc Đăng"));

// arrow-function 
const hello = (userName) => "hello" + userName;
console.log(hello("Ngọc Ánh"));

const sum = (a, b) => {
    let c = a + b;
    return c;
}
console.log(sum(10, 5));

